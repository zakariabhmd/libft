#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	zed;

	zed = 0;
	i = 0;
	while (src[zed] != '\0')
		zed++;
	if (dstsize == 0)
		return (zed);
	while (src[i] != '\0' && i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (zed);
}
